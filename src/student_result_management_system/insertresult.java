    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String physics= jTextField3.getText();
        String math = jTextField4.getText();
        String em = jTextField5.getText();
        String dbms = jTextField6.getText();
        String os = jTextField2.getText();
        
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Salman@123");
           
           Statement st = con.createStatement();
           
           ResultSet rs = st.executeQuery("select * from student where rollno ='"+rollno+"'");
           if(rs.next()){
               st.executeUpdate("insert into result(rollno,physics,maths,em,dbms,os) values('"+rollno+"','"+physics+"','"+math+"','"+em+"','"+dbms+"','"+os+"')");
               JOptionPane.showMessageDialog(null,"result is save succesfully!");
               setVisible(false);
               new insertresult().setVisible(true);
               
           }else{
               JOptionPane.showMessageDialog(null,"Student is not registered!");     
               
           } 
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

